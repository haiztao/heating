#import "RoleModel.h"

@implementation RoleModel
-(id)initWithDict:(NSDictionary *)dict{
    if (self = [super init]) {
        _from_id = [dict objectForKey:@"from_id"];
        _nickname = [dict objectForKey:@"nickname"];
        _role = [dict objectForKey:@"role"];
        _user_id = [dict objectForKey:@"user_id"];
    }
    return self;
}
